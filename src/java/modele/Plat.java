package modele;

/**
 *
 * @author Jonah
 */
public class Plat {
       String id;
       String nom;
       Double prix;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public Double getPrix() {
        return prix;
    }

    public void setPrix(Double prix) {
        this.prix = prix;
    }

    public String toString() {
        return "Plat{" + "id=" + id + ", nom=" + nom + ", prix=" + prix + '}';
    }
       
       
}
